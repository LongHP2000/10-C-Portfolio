// This program adds two integers and display the result

#include <iostream>
using namespace std;

// Using sequencing to programming structure to mathematically add two declared integers and display it onto screen.
int main() {
    // Variable Int declared and initialise the first integer with the value 8
    int num1 = 8;

    // Variable Int declared and initialise the second integer with the value 10
    int num2 = 10;

    // Variable Int declared and initialise third integer to store the sum of num1 and num2
    int sum = num1 + num2;

    // Output the sum onto display screen
    cout << "The sum is: " << sum << endl;

    return 0;
}
