/* This small program requires following preprocessor directives in C++, to 
tell the compiler to include input/output stream library.*/   
#include <iostream> // The input and output stream library.
using namespace std; // The standard namespace to avoid using std:: before every standard function.

int main() { // The main functionwhere the coding is excuted.

    // A simple program using sequencial execution to print a message onto screen.

    // cout is used to output text to the monitor screen.
    // << and >> are insert operators, used for sending data to output stream.
    // endl is used to create a new line in output, to make things look neater
    cout << "Coding is Cool" << endl;

    return 0; //Return 0 tells the program tha it has ran the program successfully.
}